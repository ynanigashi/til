def count_digits(n):
    """
    整数 n の桁数をカウントする関数
    """
    # 整数を絶対値に変換（負の数の場合を考慮）
    n = abs(n)

    # カウントアップするための変数
    count = 0

    # 整数が0になるまで10で割り続ける
    while n > 0:
        n //= 10  # 整数を10で割り、結果を整数にする
        count += 1  # カウンターを増やす

    # 整数が1桁の場合、ループは実行されないため、カウントを1に設定
    if count == 0:
        count = 1

    return count

